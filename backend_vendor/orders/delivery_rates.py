def get_all_locations():
    """Get all locations with their delivery rates"""
    return DELIVERY_RATES

def get_rate_by_location(location_name):
    """Get delivery rate for a specific location"""
    for item in DELIVERY_RATES:
        if item['location'].lower() == location_name.lower():
            return item['rate']
    return 0

def get_districts():
    """Get unique list of districts"""
    districts = sorted(set(item['district'] for item in DELIVERY_RATES))
    return districts
